// and operator (&&): If all condition true then if statement will be executed.
if(5 > 4 && 4 < 5){
  console.log('Condition true');
}
else{
  console.log('Condition false');
}

// or operator: If any of the condition true then if statement will be executed
if(9 > 8 || 8 !== 8){
  console.log('Condition true');
}
else{
  console.log('Condition false');
}
